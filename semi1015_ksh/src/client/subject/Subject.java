package client.subject;

public class Subject {
	int subNo;
	String subTime;
	String subCategory;
	String subName;
	int subYear;
	int subGrade;
	String subProfessor;
	String subSchedule;
	String subClass;
	String subEtc;
	String str;
	
	public String getStr() 							{		return str;	}
	public void setStr(String str) 					{		this.str = str;	}
	public int getSubNo() 								{	return subNo;						}
	public void setSubNo(int subNo) 					{	this.subNo = subNo;					}
	public String getSubTime() 							{	return subTime;						}
	public void setSubTime(String subTime) 				{	this.subTime = subTime;				}
	public String getSubCategory() 						{	return subCategory;					}
	public void setSubCategory(String subCategory) 		{	this.subCategory = subCategory;		}
	public String getSubName() 							{	return subName;						}
	public void setSubName(String subName) 				{	this.subName = subName;				}
	public int getSubYear() 							{	return subYear;						}
	public void setSubYear(int subYear) 				{	this.subYear = subYear;				}
	public int getSubGrade() 							{	return subGrade;					}
	public void setSubGrade(int subGrade) 				{	this.subGrade = subGrade;			}
	public String getSubProfessor() 					{	return subProfessor;				}
	public void setSubProfessor(String subProfessor) 	{	this.subProfessor = subProfessor;	}
	public String getSubSchedule() 						{	return subSchedule;					}
	public void setSubSchedule(String sunSchedule) 		{	this.subSchedule = sunSchedule;		}
	public String getSubClass() 						{	return subClass;					}
	public void setSubClass(String subClass) 			{	this.subClass = subClass;			}
	public String getSubEtc() 							{	return subEtc;						}
	public void setSubEtc(String subEtc) 				{	this.subEtc = subEtc;				}
}
